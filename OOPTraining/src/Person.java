public class Person {
    protected void getJob() {
        String job = "Engineer";
        System.out.println(job);
    }
    protected int getAge() {
        int age = 15;
        System.out.println(age);
        return age;
    }
    protected String getSex() {
        String sex = "Male";
        return sex;
    }
    protected void getPosition () {
        String position = "Officer";
        System.out.println(position);
    }
}
